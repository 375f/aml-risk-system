# ---------------------------------------------------------------------------
# Хелперы (не фикстуры — можно вызывать напрямую в тестах)
# ---------------------------------------------------------------------------

def make_df(rows: list) -> pd.DataFrame:
    """Создать канонический DataFrame (выход parser.parse_statement) из списка строк."""
    df = pd.DataFrame(
        rows,
        columns=["date", "amount", "type", "description", "counterparty", "inn"],
    )
    df["date"]   = pd.to_datetime(df["date"])
    df["amount"] = df["amount"].astype(float)
    df["description"] = df["description"].fillna("").astype(str)
    df["counterparty"] = df["counterparty"].fillna("").astype(str)
    df["inn"]    = df["inn"].fillna("").astype(str)
    return df


def csv_buf(content: str, encoding: str = "utf-8") -> io.BytesIO:
    """Создать in-memory CSV-файл с именем test.csv."""
    buf = io.BytesIO(content.encode(encoding))
    buf.name = "test.csv"
    return buf


